def cuadrado():
    valor = int(input("Ingrese el valor"))
    res = valor*valor
    print("El cuadrado es: ", res)

def producto():
    valor1 = int(input("Ingrese el primer valor"))
    valor2 = int(input("Ingrese segundo valor"))
    res = valor1 * valor2
    print("El Producto: ", res)


cuadrado()
producto()